package com.mycompany.aritmatika3main;

/**
 *
 * @author HP
 */
public class Aritmatika3Main {

    public static void main(String[] args) {
        // Nilai bilangan1 dan bilangan2 (ganti dengan nilai yang sesuai)
        double bilangan1 = 10.0;
        double bilangan2 = 5.0;

        // Pengurangan
        double hasilPengurangan = bilangan1 - bilangan2;
        System.out.println("Hasil Pengurangan: " + hasilPengurangan);

        // Perkalian
        double hasilPerkalian = bilangan1 * bilangan2;
        System.out.println("Hasil Perkalian: " + hasilPerkalian);

        // Pembagian
        double hasilPembagian = bilangan1 / bilangan2;
        System.out.println("Hasil Pembagian: " + hasilPembagian);

        // Pangkat
        double hasilPangkat = Math.pow(bilangan1 , bilangan2);
        System.out.println("Hasil Pangkat: " + hasilPangkat);
    }
}

    

